package com.liferay.portlet.messageboards;

public class TooLongTerritoryFieldValueException extends MessageValidationException {

	public TooLongTerritoryFieldValueException() {
		super();
	}

	public TooLongTerritoryFieldValueException(String msg) {
		super(msg);
	}

	public TooLongTerritoryFieldValueException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public TooLongTerritoryFieldValueException(Throwable cause) {
		super(cause);
	}

}